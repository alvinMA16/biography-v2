package prompt

// TopicPrompt 话题生成 prompt
const TopicPrompt = `你在为一个回忆录产品生成“下一步最值得聊的话题”。

你的目标不是凑出一组看起来丰富的话题，而是挑出 {{.Count}} 个用户一看到就容易开口、并且值得继续讲下去的话题入口。

你只能依据以下材料生成：
1. 用户资料中的明确信息
2. 用户长期记忆
3. 已有回忆录摘要
4. 已有话题标题（用于避免重复）

请遵守以下原则：
1. 第一标准永远是“用户能接得住、愿意聊”。
2. 每个话题都必须和用户已经明确讲过的人生经历有真实关联，不要靠年代印象、地域印象、常见生活意象去猜。
3. 禁止无根据生成这类内容：季节意象、庙会、书信、小电器、某种食物、某种节庆习俗、某种城市记忆，除非材料里明确出现过。
4. 话题要是一个具体入口，不要抽象，不要像作文题。
5. 可以围绕某段经历继续往下聊，但前提是这段经历还有明显没聊开的空间。
6. 整组话题放在一起，要尽量覆盖用户人生中不同的区域，避免都挤在同一段经历里。
7. 不要为了“全面”而凑数，宁可少而准。
8. 如果某个方向虽然听起来有画面感，但和这个用户没有明确关系，就不要生成。
9. 输出的话题应该让人一看就知道怎么开口，不需要额外解释。

## 用户资料
- 称呼：{{.UserName}}
- 出生年份：{{.BirthYear}}
- 家乡：{{.Hometown}}
- 主要生活城市：{{.MainCity}}

## 用户长期记忆
{{.UserMemory}}

## 已有回忆录摘要
{{.MemoirSummaries}}

## 已有话题标题（避免重复）
{{.ExistingTopics}}

每个话题输出 3 个字段：
- title: 给用户看的话题标题，具体、自然、可开口
- greeting: 一句自然的开场白
- context: 给系统看的内部提示，说明这个话题为什么值得聊、可以往哪边展开

输出格式：
[
  {
    "title": "...",
    "greeting": "...",
    "context": "..."
  }
]`

