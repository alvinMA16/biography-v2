// TopicPrompt 话题生成 prompt
const TopicPrompt = `你在为一个回忆录产品生成“下一步最值得聊的话题”。

你的目标不是凑出一组看起来丰富的话题，而是挑出 {{.Count}} 个用户一看到就容易开口、并且值得继续讲下去的话题入口。

你只能依据以下材料生成：
1. 用户资料中的明确信息
2. 用户长期记忆
3. 已有回忆录摘要
4. 已有话题标题（用于避免重复）

请遵守以下原则：
1. 第一标准永远是“用户能接得住、愿意聊”。
2. 每个话题都必须和用户的已知信息有真实关联。这个已知信息既可以来自用户已经讲过的人生经历，也可以来自用户资料里明确可知的稳定背景信息。
3. 禁止无根据生成这类内容：季节意象、庙会、书信、小电器、某种食物、某种节庆习俗、某种城市记忆，除非材料里明确出现过。
4. 话题要是一个具体入口，不要抽象，不要像作文题。
5. 先判断用户的人生哪些区域已经聊得比较充分，哪些区域还是明显空白。当前期人生覆盖还不完整时，优先补那些还没讲出来的重要人生阶段和经历。
6. 允许少量围绕已聊经历继续往下聊，但最多只能占整组话题里的 1-2 个，而且前提是那段经历确实还有明显没聊开的空间。
7. 整组话题放在一起，要主动拉开人生覆盖面，优先让用户的人生版图更完整，而不是都挤在已经讲过的那一段里。
8. 如果用户还有大片人生空白没有触达，就不要把大多数话题都继续压在已聊内容上。
9. 不要为了“全面”而凑数，宁可少而准。
10. 如果某个方向虽然听起来有画面感，但和这个用户没有明确关系，就不要生成。
11. 输出的话题应该让人一看就知道怎么开口，不需要额外解释。

在生成前，请先默默完成这一步判断：
- 用户已经比较明确讲过的人生区域有哪些
- 用户还明显没有展开的人生区域有哪些
- 这次生成的话题里，应该优先补哪些空白

优先补空白的人生区域，常见可以包括但不限于：
- 童年和成长环境
- 读书和求学阶段
- 第一份工作或早期职业选择
- 创业、转行、搬家、婚育、家庭责任等关键转折
- 某段后来影响很深、但还没讲开的时期

请记住：
- 不要为了“避免猜测”就只敢围绕已聊内容打转
- 也不要为了“补空白”而乱猜具体生活细节
- 好的话题，是既有依据，又能把用户带到还没讲出来的重要人生区域

## 用户资料
- 称呼：{{.UserName}}
- 出生年份：{{.BirthYear}}
- 家乡：{{.Hometown}}
- 主要生活城市：{{.MainCity}}

## 用户长期记忆
{{.UserMemory}}

## 已有回忆录摘要
{{.MemoirSummaries}}

## 已有话题标题（避免重复）
{{.ExistingTopics}}

每个话题输出 3 个字段：
- title: 给用户看的话题标题，具体、自然、可开口
- greeting: 一句自然的开场白
- context: 给系统看的内部提示，说明这个话题为什么值得聊、可以往哪边展开

输出格式：
[
  {
    "title": "...",
    "greeting": "...",
    "context": "..."
  }
]`

